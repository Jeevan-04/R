# Basic variable assignment
my_var <- 10                # Creates a numeric variable
another_var <- "Hello"      # Creates a character variable
is_true <- TRUE             # Creates a logical variable

# Print / Output Variables
print(my_var)               # Prints: [1] 10
print(another_var)          # Prints: [1] "Hello"
print(is_true)              # Prints: [1] TRUE

# Explanation:
# - The assignment operator `<-` is standard in R, although `=` can also be used (my_var = 10).
# - Variable names are case-sensitive, meaning `MyVar` and `myvar` are different variables.
